#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	resto;

	if (nb < 0)
	{
		write(1, &"-", 1);
		if (nb == -2147483648)
		{
			write(1, "2147483648", 10);
			return ;
		}
		nb = nb * (-1);
	}
	if (nb < 10)
	{
		nb = nb + 48;
		write(1, &nb, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		resto = (nb % 10) + 48;
		write(1, &resto, 1);
	}
}
